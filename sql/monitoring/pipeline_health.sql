create or replace view MARKETING_DATA_QUALITY_DB.MONITORING.PIPELINE_HEALTH(
	TOTAL_TESTS,
	PASSED_TESTS,
	FAILED_TESTS,
	QUALITY_SCORE,
	PIPELINE_STATUS
) as

SELECT

COUNT(*) AS TOTAL_TESTS,

SUM(
CASE
WHEN STATUS = 'PASS'
THEN 1
ELSE 0
END
) AS PASSED_TESTS,

SUM(
CASE
WHEN STATUS = 'FAIL'
THEN 1
ELSE 0
END
) AS FAILED_TESTS,

ROUND(
(
SUM(
CASE
WHEN STATUS = 'PASS'
THEN 1
ELSE 0
END
) * 100.0
) / COUNT(*),
2
) AS QUALITY_SCORE,

CASE
WHEN SUM(
CASE
WHEN STATUS = 'FAIL'
THEN 1
ELSE 0
END
) = 0
THEN 'HEALTHY'

WHEN SUM(
CASE
WHEN STATUS = 'FAIL'
THEN 1
ELSE 0
END
) <= 5
THEN 'WARNING'

ELSE 'CRITICAL'
END AS PIPELINE_STATUS

FROM MONITORING.DATA_QUALITY_RESULTS;
