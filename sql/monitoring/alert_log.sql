create or replace view MARKETING_DATA_QUALITY_DB.MONITORING.ALERT_LOG(
	TEST_NAME,
	TABLE_NAME,
	STATUS,
	METRIC_VALUE,
	ALERT_TIMESTAMP,
	ALERT_SEVERITY
) as

SELECT
TEST_NAME,
TABLE_NAME,
STATUS,
METRIC_VALUE,

CURRENT_TIMESTAMP() AS ALERT_TIMESTAMP,

CASE
WHEN TEST_NAME IN (
'REFERENTIAL_INTEGRITY_CHECK',
'REVENUE_RECONCILIATION'
)
THEN 'HIGH'

WHEN TEST_NAME IN (
'NULL_CHECK',
'DUPLICATE_CHECK'
)
THEN 'MEDIUM'

ELSE 'LOW'
END AS ALERT_SEVERITY

FROM MONITORING.DATA_QUALITY_RESULTS

WHERE STATUS = 'FAIL';
