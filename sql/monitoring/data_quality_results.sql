create or replace view MARKETING_DATA_QUALITY_DB.MONITORING.DATA_QUALITY_RESULTS(
	TEST_NAME,
	TABLE_NAME,
	STATUS,
	METRIC_VALUE
) as

SELECT
'ROW_COUNT_CHECK' AS TEST_NAME,
TABLE_NAME,
STATUS,
CURRENT_ROW_COUNT AS METRIC_VALUE
FROM QUALITY.INT_ROW_COUNT_CHECKS

UNION ALL

SELECT
'NULL_CHECK',
TABLE_NAME || ' - ' || COLUMN_NAME,
STATUS,
NULL_COUNT
FROM QUALITY.INT_NULL_CHECKS

UNION ALL

SELECT
'DUPLICATE_CHECK',
TABLE_NAME || ' - ' || KEY_COLUMN,
STATUS,
DUPLICATE_COUNT
FROM QUALITY.INT_DUPLICATE_CHECKS

UNION ALL

SELECT
'ACCEPTED_VALUES_CHECK',
TABLE_NAME || ' - ' || COLUMN_NAME,
STATUS,
INVALID_COUNT
FROM QUALITY.INT_ACCEPTED_VALUES_CHECKS

UNION ALL

SELECT
'REFERENTIAL_INTEGRITY_CHECK',
CHECK_NAME,
STATUS,
FAILED_RECORDS
FROM QUALITY.INT_REFERENTIAL_INTEGRITY_CHECKS

UNION ALL

SELECT
'FRESHNESS_CHECK',
TABLE_NAME,
STATUS,
DAYS_DELAYED
FROM QUALITY.INT_FRESHNESS_CHECKS

UNION ALL

SELECT
'VOLUME_ANOMALY_CHECK',
TABLE_NAME,
STATUS,
VARIANCE_PERCENT
FROM QUALITY.INT_VOLUME_ANOMALY_CHECKS

UNION ALL

SELECT
'REVENUE_RECONCILIATION',
CHECK_NAME,
STATUS,
REVENUE_DIFFERENCE
FROM QUALITY.INT_REVENUE_RECONCILIATION;
