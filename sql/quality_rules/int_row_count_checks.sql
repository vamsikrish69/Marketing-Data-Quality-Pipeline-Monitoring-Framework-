create or replace view MARKETING_DATA_QUALITY_DB.QUALITY.INT_ROW_COUNT_CHECKS(
	TABLE_NAME,
	CURRENT_ROW_COUNT,
	EXPECTED_MIN_ROWS,
	STATUS
) as

SELECT
'CAMPAIGNS' AS TABLE_NAME,
COUNT(*) AS CURRENT_ROW_COUNT,
25 AS EXPECTED_MIN_ROWS,
CASE
WHEN COUNT(*) >= 25 THEN 'PASS'
ELSE 'FAIL'
END AS STATUS
FROM STAGING.STG_CAMPAIGNS

UNION ALL

SELECT
'CUSTOMERS',
COUNT(*),
500,
CASE
WHEN COUNT(*) >= 500 THEN 'PASS'
ELSE 'FAIL'
END
FROM STAGING.STG_CUSTOMERS

UNION ALL

SELECT
'MARKETING_TOUCHPOINTS',
COUNT(*),
5500,
CASE
WHEN COUNT(*) >= 5500 THEN 'PASS'
ELSE 'FAIL'
END
FROM STAGING.STG_MARKETING_TOUCHPOINTS

UNION ALL

SELECT
'CONVERSIONS',
COUNT(*),
800,
CASE
WHEN COUNT(*) >= 800 THEN 'PASS'
ELSE 'FAIL'
END
FROM STAGING.STG_CONVERSIONS

UNION ALL

SELECT
'AD_SPEND',
COUNT(*),
2250,
CASE
WHEN COUNT(*) >= 2250 THEN 'PASS'
ELSE 'FAIL'
END
FROM STAGING.STG_AD_SPEND;
